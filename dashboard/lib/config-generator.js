import { writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ASTERISK_DIR = join(__dirname, '..', '..', 'asterisk');

function contextName(type, id) {
  return `${type}-${id}`;
}

export function generateExtensionsConf(store) {
  const data = store.getAll();
  const { didRoutes, ivrMenus, ringGroups, globals } = data;
  const lines = [];

  lines.push('; ================================');
  lines.push('; DID routing + IVR dialplan');
  lines.push('; ================================');
  lines.push('; Auto-generated by Asterisk Dashboard');
  lines.push('; Do not edit manually - changes will be overwritten');
  lines.push('');
  lines.push('[general]');
  lines.push('static=yes');
  lines.push('writeprotect=no');
  lines.push('clearglobalvars=no');
  lines.push('');
  lines.push('[globals]');
  lines.push(`IVR_RESPONSE_TIMEOUT=${globals.ivrResponseTimeout}`);
  lines.push(`IVR_DIGIT_TIMEOUT=${globals.ivrDigitTimeout}`);
  lines.push('');

  // Inbound context
  lines.push('[from-supplier-ip]');
  lines.push('; Most providers deliver DID in Request-URI as ${EXTEN}');
  lines.push('exten => _X!,1,NoOp(Inbound call from supplier. Raw EXTEN=${EXTEN})');
  lines.push(' same => n,Set(DID=${FILTER(0-9,${EXTEN})})');
  lines.push(' same => n,GotoIf($["${DID}" = ""]?extract_from_to_header,1)');
  lines.push(' same => n,Goto(did-routing,${DID},1)');
  lines.push('');
  lines.push('; Some providers include a leading "+" in DID');
  lines.push('exten => _+X!,1,NoOp(Inbound call from supplier. Raw EXTEN=${EXTEN})');
  lines.push(' same => n,Set(DID=${FILTER(0-9,${EXTEN})})');
  lines.push(' same => n,GotoIf($["${DID}" = ""]?extract_from_to_header,1)');
  lines.push(' same => n,Goto(did-routing,${DID},1)');
  lines.push('');
  lines.push('; Fallback if provider sends call to \'s\' and DID is in SIP To header');
  lines.push('exten => extract_from_to_header,1,NoOp(Extract DID from SIP To header)');
  lines.push(' same => n,Set(TO_HDR=${PJSIP_HEADER(read,To)})');
  lines.push(' same => n,Set(DID_RAW=${CUT(CUT(${TO_HDR},@,1),:,2)})');
  lines.push(' same => n,Set(DID=${FILTER(0-9,${DID_RAW})})');
  lines.push(' same => n,GotoIf($["${DID}" = ""]?no-did,1)');
  lines.push(' same => n,Goto(did-routing,${DID},1)');
  lines.push('');
  lines.push('exten => s,1,Goto(extract_from_to_header,1)');
  lines.push('');
  lines.push('exten => no-did,1,NoOp(No DID found in request -- rejecting call)');
  lines.push(' same => n,Playback(ss-noservice)');
  lines.push(' same => n,Hangup()');
  lines.push('');

  // DID routing context
  lines.push('[did-routing]');
  for (const route of didRoutes) {
    let target;
    if (route.destinationType === 'ivr') {
      const ivr = ivrMenus.find(m => m.id === route.destinationId);
      target = ivr ? `ivr-${ivr.id}` : 'ivr-1';
      lines.push(`exten => ${route.didNumber},1,NoOp(Route DID \${EXTEN} to ${route.description || 'IVR'})`);
      lines.push(` same => n,Goto(${target},s,1)`);
    } else if (route.destinationType === 'ring_group') {
      const rg = ringGroups.find(g => g.id === route.destinationId);
      target = rg ? `ring-group-${rg.id}` : 'ring-group-1';
      lines.push(`exten => ${route.didNumber},1,NoOp(Route DID \${EXTEN} to ${route.description || 'Ring Group'})`);
      lines.push(` same => n,Goto(${target},s,1)`);
    } else {
      lines.push(`exten => ${route.didNumber},1,NoOp(Route DID \${EXTEN} to ${route.description || 'Direct'})`);
      lines.push(` same => n,Dial(PJSIP/${route.destinationId},30)`);
      lines.push(` same => n,Hangup()`);
    }
    lines.push('');
  }
  lines.push('exten => _X.,1,NoOp(Unknown or unmapped DID: ${EXTEN})');
  lines.push(' same => n,Playback(ss-noservice)');
  lines.push(' same => n,Hangup()');
  lines.push('');

  // IVR contexts
  for (const ivr of ivrMenus) {
    lines.push(`[ivr-${ivr.id}]`);
    lines.push(`exten => s,1,NoOp(${ivr.name} IVR start)`);
    lines.push(' same => n,Answer()');
    lines.push(' same => n,Set(TIMEOUT(response)=${IVR_RESPONSE_TIMEOUT})');
    lines.push(' same => n,Set(TIMEOUT(digit)=${IVR_DIGIT_TIMEOUT})');
    lines.push(` same => n(loop),Background(${ivr.audioFile})`);
    lines.push(' same => n,WaitExten(5)');
    lines.push('');

    for (const opt of (ivr.options || [])) {
      if (opt.actionType === 'ring_group') {
        lines.push(`exten => ${opt.digit},1,Goto(ring-group-${opt.actionTarget},s,1)`);
      } else if (opt.actionType === 'ivr') {
        lines.push(`exten => ${opt.digit},1,Goto(ivr-${opt.actionTarget},s,1)`);
      } else {
        lines.push(`exten => ${opt.digit},1,Dial(PJSIP/${opt.actionTarget},30)`);
      }
    }
    lines.push('');
    lines.push('exten => i,1,Playback(pbx-invalid)');
    lines.push(' same => n,Goto(s,loop)');
    lines.push('');
    lines.push('exten => t,1,Playback(vm-goodbye)');
    lines.push(' same => n,Hangup()');
    lines.push('');
  }

  // Ring group contexts
  for (const rg of ringGroups) {
    lines.push(`[ring-group-${rg.id}]`);
    lines.push(`exten => s,1,NoOp(Dial ${rg.name})`);
    const dialTargets = rg.extensions.map(e => `PJSIP/${e}`).join('&');
    lines.push(` same => n,Dial(${dialTargets},${rg.ringTimeout})`);
    if (rg.voicemailExt) {
      lines.push(` same => n,Voicemail(${rg.voicemailExt}@default,u)`);
    }
    lines.push(' same => n,Hangup()');
    lines.push('');
  }

  // Outbound route
  lines.push('; Optional outbound route using same IP-auth trunk');
  lines.push('[from-internal]');
  lines.push('exten => _X.,1,NoOp(Outbound call via supplier trunk to ${EXTEN})');
  lines.push(' same => n,Dial(PJSIP/${EXTEN}@supplier-trunk,60)');
  lines.push(' same => n,Hangup()');
  lines.push('');

  return lines.join('\n');
}

export function generatePjsipConf(store) {
  const trunk = store.getTrunkConfig();
  const lines = [];

  lines.push('; ================================');
  lines.push('; PJSIP trunk (IP-to-IP authentication)');
  lines.push('; ================================');
  lines.push('; Auto-generated by Asterisk Dashboard');
  lines.push('');
  lines.push('[global]');
  lines.push('type=global');
  lines.push(`user_agent=${trunk.userAgent}`);
  lines.push('');
  lines.push('[transport-udp]');
  lines.push('type=transport');
  lines.push('protocol=udp');
  lines.push(`bind=0.0.0.0:${trunk.bindPort}`);
  lines.push(`external_media_address=${trunk.publicIp}`);
  lines.push(`external_signaling_address=${trunk.publicIp}`);
  lines.push('local_net=10.0.0.0/8');
  lines.push('local_net=172.16.0.0/12');
  lines.push('local_net=192.168.0.0/16');
  lines.push('');
  lines.push('[supplier-trunk]');
  lines.push('type=endpoint');
  lines.push('transport=transport-udp');
  lines.push('context=from-supplier-ip');
  lines.push('disallow=all');
  lines.push(`allow=${trunk.codecs.join(',')}`);
  lines.push('aors=supplier-trunk-aor');
  lines.push('direct_media=no');
  lines.push('rtp_symmetric=yes');
  lines.push('force_rport=yes');
  lines.push('rewrite_contact=yes');
  lines.push('trust_id_inbound=yes');
  lines.push('send_pai=yes');
  lines.push('t38_udptl=no');
  lines.push('');
  lines.push('[supplier-trunk-aor]');
  lines.push('type=aor');
  lines.push(`contact=sip:${trunk.supplierIp}:${trunk.bindPort}`);
  lines.push(`qualify_frequency=${trunk.qualifyFrequency}`);
  lines.push('');
  lines.push('[supplier-trunk-identify]');
  lines.push('type=identify');
  lines.push('endpoint=supplier-trunk');
  lines.push(`match=${trunk.supplierIp}/32`);
  lines.push('');

  return lines.join('\n');
}

export function writeConfigs(store) {
  const extConf = generateExtensionsConf(store);
  const pjsipConf = generatePjsipConf(store);

  writeFileSync(join(ASTERISK_DIR, 'extensions.conf'), extConf, 'utf8');
  writeFileSync(join(ASTERISK_DIR, 'pjsip.conf'), pjsipConf, 'utf8');

  return { extensionsConf: extConf, pjsipConf: pjsipConf };
}
